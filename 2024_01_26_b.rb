# 配列の要素を条件付きで選択：
# 配列 [1, 2, 3, 4, 5, 6] から3より大きい要素のみを選択するRubyのコードは？
ar = [1, 2, 3, 4, 5, 6]
puts ar.select{|i| i > 3 }

# ハッシュのキーと値を反転：
# ハッシュ { a: 1, b: 2, c: 3 } のキーと値を反転させるRubyのコードは？
h = { a: 1, b: 2, c: 3 }
puts h.invert

# 文字列の各文字に対する繰り返し処理：
# 文字列 "hello" の各文字を順番に出力するRubyのコードは？
"hello".each_char { |c| puts c }
